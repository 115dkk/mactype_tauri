import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const schema = JSON.parse(fs.readFileSync(path.join(root, "shared", "settings-schema.json"), "utf8"));
const rustNumber = (value) => Number.isInteger(value) ? `${value}.0` : String(value);

const ts = `// Generated by scripts/generate-settings.mjs. Do not edit.\n` +
  `export const settingsSchema = ${JSON.stringify(schema, null, 2)} as const;\n\n` +
  `export type SettingDefinition = (typeof settingsSchema)[number];\n` +
  `export type SettingId = SettingDefinition["id"];\n`;

const rustRows = schema.map((item) =>
  `    SettingDefinition {\n` +
  `        id: ${JSON.stringify(item.id)},\n` +
  `        section: ${JSON.stringify(item.section)},\n` +
  `        key: ${JSON.stringify(item.key)},\n` +
  `        value_type: SettingValueType::${item.type === "float" ? "Float" : "Integer"},\n` +
  `        min: ${rustNumber(item.min)},\n` +
  `        max: ${rustNumber(item.max)},\n` +
  `        default: ${rustNumber(item.default)},\n` +
  `        previewable: ${item.previewable},\n` +
  `        ordinal: ${item.legacy_control_center_ordinal},\n` +
  `    },`,
).join("\n");
const rust = `// Generated by scripts/generate-settings.mjs. Do not edit.\n` +
  `#[derive(Clone, Copy, Debug, PartialEq)]\npub enum SettingValueType {\n    Integer,\n    Float,\n}\n\n` +
  `#[allow(dead_code)]\n#[derive(Clone, Copy, Debug)]\npub struct SettingDefinition {\n    pub id: &'static str,\n    pub section: &'static str,\n    pub key: &'static str,\n    pub value_type: SettingValueType,\n    pub min: f64,\n    pub max: f64,\n    pub default: f64,\n    pub previewable: bool,\n    pub ordinal: i32,\n}\n\n` +
  `pub const SETTINGS: &[SettingDefinition] = &[\n${rustRows}\n];\n`;

const cppRows = schema.map((item) =>
  `    {${JSON.stringify(item.id)}, ${item.legacy_control_center_ordinal}, ${item.type === "float" ? "true" : "false"}},`,
).join("\n");
const cpp = `// Generated by scripts/generate-settings.mjs. Do not edit.\n#pragma once\n\n#include <array>\n\nnamespace mactype {\nstruct SettingDefinition { const char* id; int ordinal; bool is_float; };\ninline constexpr std::array<SettingDefinition, ${schema.length}> kSettings{{\n${cppRows}\n}};\n}  // namespace mactype\n`;

const outputs = [
  [path.join(root, "control-center", "src", "generated", "settings.ts"), ts],
  [path.join(root, "control-center", "src-tauri", "src", "generated_settings.rs"), rust],
  [path.join(root, "preview-helper", "include", "generated_settings.h"), cpp],
];
for (const [output, content] of outputs) {
  fs.mkdirSync(path.dirname(output), { recursive: true });
  fs.writeFileSync(output, content);
}
